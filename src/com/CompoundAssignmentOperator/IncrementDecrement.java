package com.CompoundAssignmentOperator;

public class IncrementDecrement {
	public static void main(String[] args) {
		// Increment or decrement is used to either increase or decrease  an original value of variable by value 1.
		int x = 10;
		System.out.println(x++);
		System.out.println(x);
		System.out.println(x--);
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x);
		System.out.println(--x);
		System.out.println(x);
	}
}
