package com.TypeCasting;

public class Widening {
	public static void main(String[] args) {
		
		// widening is a process to convert smaller primitive data type into bigger  data type in widening there will be no data loss ,it is done automatically so it is also called implicit conversion
		char x = 'a';  
		int y = x;
		System.out.println("X is : "+x);
		System.out.println("Y is : "+y);
	}

}
